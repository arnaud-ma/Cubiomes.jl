function test_perlin_creation(perlin_test, rng)
    rng2 = copy(rng)
    perlin = NoiseğŸ²(Perlin, rng)
    perlin2 = Noise(Perlin, undef)
    set_rng!ğŸ²(perlin2, rng2)

    @test perlin == perlin_test
    @test perlin2 == perlin_test
end

